 4. “게임세계”라 함은 게임서비스를 통해 다중의 회원이 일정한 규칙(이하 “게임규칙”)에 따라 오락을 하거나 오락에 부수하여 여가선용, 친목도모, 정보매개 등을 할 수 있도록 게임성을 구현한 가변적인 가상세계를 의미합니다.

