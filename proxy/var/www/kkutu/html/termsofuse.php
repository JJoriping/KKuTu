제1장 총칙

 

제1조 (목적) 이 약관은 RomanHue(이하 “운영자”)가 온라인으로 제공하는 게임 및 이에 부수된 제반 서비스(이하 “게임서비스”)의 이용과 관련하여 운영자와 회원 간의 권리, 의무 및 책임사항, 기타 필요한 사항을 규정함을 목적으로 합니다.

 

