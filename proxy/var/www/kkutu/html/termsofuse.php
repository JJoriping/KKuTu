제4조 (약관의 명시와 개정) ① 운영자는 이 약관의 내용을 회원이 알 수 있도록  게임 초기 화면이나 게임서비스 홈페이지(www.000.co.kr)에 게시하거나 연결화면을 제공하는 방법으로 회원에게 공지합니다.

② 운영자는 회원이 운영자와 이 약관의 내용에 관하여 질의 및 응답을 할 수 있도록 조치를 취합니다.

